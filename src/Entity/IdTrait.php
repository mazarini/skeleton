namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;

trait IdTrait
{
    /**
     * @var int
     *
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id = 0;

    public function getId(): int
    {
        return $this->id;
    }

    public function isNew(): bool
    {
        return 0 === $this->id;
    }
}
